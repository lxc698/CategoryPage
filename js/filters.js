function nikeCheckbox() {
  var checkBox = document.getElementById("nike-checkbox");
  var text = document.getElementById("nike-cancel");
  if (checkBox.checked == true){
    text.style.display = "inline-block";
  } else {
    text.style.display = "none";
  }
}

